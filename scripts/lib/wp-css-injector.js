/**
 * scripts/lib/wp-css-injector.js
 * UMBAUPLAN v2.0 Phase 3.1 — Workaround for Elementor 4.1.0-beta1 0-byte CSS bug.
 *
 * Symptom (E2E-Test 2026-06-15):
 *   `Post::update()` schreibt 0-byte CSS-File → Inline-Styles werden nicht kompiliert
 *   → Elementor rendert den V4-Tree ohne jegliches Styling.
 *
 * Workaround:
 *   1. Generiert /wp-content/uploads/elementor-custom-css/post-{post_id}.css
 *   2. Erstellt/updated mu-plugin /wp-content/mu-plugins/elementor-custom-css-{post_id}.php
 *   3. mu-plugin enqueued die CSS-Datei NUR auf diesem post_id
 *
 * API:
 *   const injector = createWpCssInjector({ mcpBridge, siteId });
 *   await injector.injectCustomCss({ post_id: 123, css: '...' });
 *   await injector.removeCustomCss({ post_id: 123 });
 *
 * Tests: tests/lib/wp-css-injector.test.js (mock-basiert, keine Live-MCP nötig)
 */

import { createHash } from 'node:crypto';

// ─────────────────────────────────────────────
// CSS GENERATION HELPERS
// ─────────────────────────────────────────────

/**
 * Generiert CSS-Output aus einem V4-Style-Tree.
 *
 * Mappt {styles: {styleId: {variants: [{meta, props}]}}} auf flachen CSS-String.
 * Jede Variante wird zu einer Selector-Block mit Media-Query (für breakpoint) +
 * Pseudo-Class-Selector (für state) konvertiert.
 *
 * @param {object} tree - V4-Element-Tree
 * @returns {string} Generated CSS
 */
export function generateCssFromV4Tree(tree) {
  if (!tree || typeof tree !== 'object') return '';
  const blocks = [];
  walkForCss(tree, blocks, []);
  return blocks.join('\n\n');
}

function walkForCss(node, blocks, ancestors) {
  if (!node || typeof node !== 'object') return;
  if (node.styles && typeof node.styles === 'object') {
    for (const [styleId, styleDef] of Object.entries(node.styles)) {
      const variants = styleDef.variants || [];
      for (const variant of variants) {
        const css = renderVariant(styleId, variant);
        if (css.trim()) blocks.push(css);
      }
    }
  }
  const children = node.elements || node.children || [];
  for (const child of children) walkForCss(child, blocks, [...ancestors, node]);
}

function renderVariant(classId, variant) {
  if (!variant || !variant.props) return '';
  const lines = [];
  for (const [prop, value] of Object.entries(variant.props)) {
    const cssVal = unwrapV4Value(value);
    if (cssVal) lines.push(`  ${prop}: ${cssVal};`);
  }
  if (lines.length === 0) return '';
  const selector = `.${classId}`;
  const state = variant.meta?.state;
  const breakpoint = variant.meta?.breakpoint;
  let block = `${selector}${state ? ':' + state : ''} {\n${lines.join('\n')}\n}`;
  if (breakpoint && breakpoint !== 'desktop') {
    const bpQuery = breakpointToMedia(breakpoint);
    if (bpQuery) block = `@media ${bpQuery} {\n${block}\n}`;
  }
  return block;
}

function unwrapV4Value(value) {
  if (!value || typeof value !== 'object') return '';
  if (typeof value === 'string') return value;
  if (value['$$type'] === 'size') return `${value.value?.size ?? 0}${value.value?.unit ?? 'px'}`;
  if (value['$$type'] === 'string') return value.value ?? '';
  if (value['$$type'] === 'color') return value.value ?? '';
  if (value['$$type'] === 'dimensions') {
    const v = value.value || {};
    const get = (key) => unwrapV4Value(v[key]);
    return `${get('block-start')} ${get('inline-end')} ${get('block-end')} ${get('inline-start')}`;
  }
  if (value['$$type'] === 'border-radius') {
    const v = value.value || {};
    return `${unwrapV4Value(v['start-start'])} ${unwrapV4Value(v['start-end'])} ${unwrapV4Value(v['end-end'])} ${unwrapV4Value(v['end-start'])}`;
  }
  return '';
}

function breakpointToMedia(breakpoint) {
  switch (breakpoint) {
    case 'tablet':  return '(max-width: 1024px)';
    case 'mobile':  return '(max-width: 767px)';
    case 'widescreen': return '(min-width: 1440px)';
    case 'laptop':  return '(max-width: 1366px)';
    default: return null;
  }
}

// ─────────────────────────────────────────────
// MU-PLUGIN TEMPLATE
// ─────────────────────────────────────────────

/**
 * Generiert mu-plugin-PHP-Inhalt für ein post_id-spezifisches CSS-Injection.
 *
 * @param {number} postId
 * @param {string} cssFileUrl - URL zur CSS-Datei (z.B. /wp-content/uploads/elementor-custom-css/post-123.css)
 * @returns {string} PHP-File-Content
 */
export function generateMuPluginContent(postId, cssFileUrl) {
  return `<?php
/**
 * Auto-generated by novamira-adrianv2 / framer-v4-pipeline (UMBAUPLAN Phase 3.1).
 * Enqueues custom CSS for post_id=${postId} only.
 * DO NOT EDIT — will be overwritten on next build.
 */

if ( ! defined( 'ABSPATH' ) ) { exit; }

add_action( 'wp_enqueue_scripts', function () {
  if ( is_singular( ${postId} ) ) {
    wp_enqueue_style(
      'novamira-custom-css-${postId}',
      '${cssFileUrl.replace(/'/g, "\\'")}',
      array(),
      filemtime( WP_CONTENT_DIR . '/uploads/elementor-custom-css/post-${postId}.css' ) ?: '1.0'
    );
  }
} );
`;
}

// ─────────────────────────────────────────────
// INJECTOR FACTORY
// ─────────────────────────────────────────────

/**
 * Factory für WP-CSS-Injector.
 * Erwartet einen McpBridge-Client (oder mock) mit `call(name, params)` API.
 *
 * @param {object} options
 * @param {object} options.mcpBridge - { call(ability_name, parameters) }
 * @param {string} options.siteId   - 'solar' | 'test4' | 'treetsshop'
 * @returns {object} injector-API
 */
export function createWpCssInjector({ mcpBridge, siteId }) {
  if (!mcpBridge) throw new Error('createWpCssInjector: mcpBridge required');
  if (!siteId) throw new Error('createWpCssInjector: siteId required');

  /**
   * Injiziert custom CSS für einen Post.
   *
   * @param {object} opts
   * @param {number} opts.post_id
   * @param {string} opts.css - Generated CSS content
   * @returns {Promise<{css_url: string, mu_plugin_path: string, success: boolean}>}
   */
  async function injectCustomCss({ post_id, css }) {
    if (!post_id || typeof post_id !== 'number') {
      throw new Error('injectCustomCss: post_id (number) required');
    }
    if (typeof css !== 'string') {
      throw new Error('injectCustomCss: css (string) required');
    }

    const cssFilename = `post-${post_id}.css`;
    const cssFilePath = `wp-content/uploads/elementor-custom-css/${cssFilename}`;
    const cssUrl = `/wp-content/uploads/elementor-custom-css/${cssFilename}`;
    const cssHash = createHash('md5').update(css).digest('hex').slice(0, 8);
    const muPluginPath = `wp-content/mu-plugins/elementor-custom-css-${post_id}.php`;

    // 1. Upload CSS file via create-upload-link
    const uploadResult = await mcpBridge.call('create-upload-link', {
      filename: cssFilename,
      content: css,
    }).catch(err => ({ error: err.message }));

    if (uploadResult?.error) {
      return { success: false, error: `css-upload: ${uploadResult.error}` };
    }

    // 2. Create/update mu-plugin via execute-php
    const muPluginCode = generateMuPluginContent(post_id, cssUrl);
    const muResult = await mcpBridge.call('execute-php', {
      code: muPluginCode,
      path: muPluginPath,
    }).catch(err => ({ error: err.message }));

    if (muResult?.error) {
      return { success: false, error: `mu-plugin: ${muResult.error}`, css_url: cssUrl };
    }

    return {
      success: true,
      css_url: cssUrl,
      mu_plugin_path: muPluginPath,
      css_hash: cssHash,
    };
  }

  /**
   * Entfernt custom CSS für einen Post.
   *
   * @param {object} opts
   * @param {number} opts.post_id
   * @returns {Promise<{success: boolean}>}
   */
  async function removeCustomCss({ post_id }) {
    const muPluginPath = `wp-content/mu-plugins/elementor-custom-css-${post_id}.php`;
    const cssFilePath = `wp-content/uploads/elementor-custom-css/post-${post_id}.css`;

    await mcpBridge.call('execute-php', {
      code: '<?php if (file_exists($path)) unlink($path);',
      path: muPluginPath,
      unlink: true,
    }).catch(() => null);

    await mcpBridge.call('execute-php', {
      code: '<?php if (file_exists($path)) unlink($path);',
      path: cssFilePath,
      unlink: true,
    }).catch(() => null);

    return { success: true };
  }

  return {
    siteId,
    injectCustomCss,
    removeCustomCss,
  };
}
